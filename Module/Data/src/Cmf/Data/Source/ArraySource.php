namespace Cmf\Data\Source;

/**
 * @author Vital Leshchyk <vitalleshchyk@gmail.com>
 */
class ArraySource extends AbstractSource
{
    /** @var array */
    protected $data = [];

    /**
     * @param array $data
     */
    public function __construct(array $data)
    {
        $this->data = $data;
    }

    /**
     * @return array
     */
    public function getData()
    {
        return $this->data;
    }
}
